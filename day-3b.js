const solvePuzzle = (input) => {
  const rows = input.split('\n').filter(Boolean);

  const results = [
    loopPath(rows, 1, 1),
    loopPath(rows, 3, 1),
    loopPath(rows, 5, 1),
    loopPath(rows, 7, 1),
    loopPath(rows, 1, 2),
  ];

  return results.reduce((acc, curr) => curr ? curr * acc : acc)
}

const loopPath = (rows, deltaX, deltaY) => {
  let currentX = 0,
      currentY = 0;

  let trees = 0;
  while (currentY < rows.length) {
    if (isTree(rows, currentX, currentY)) {
        trees++;
    }

    currentX += deltaX;
    currentY += deltaY;
  }

  return trees;
}

const isTree = (rows, x, y) => {
  x = x % 31;

  const row = rows[y];

  return row[x] === '#';
}

